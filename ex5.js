let textoOriginal = "A programação é divertida!";

let textoMaiusculas = textoOriginal.toUpperCase();

let textoMinusculas = textoOriginal.toLowerCase();

let textoFatiado = textoOriginal.slice(0, 10);

let textoSubstituido = textoOriginal.replace("divertido", "legal");

console.log("Texto original:", textoOriginal);
console.log("Texto em maiúsculas:", textoMaiusculas);
console.log("Texto em minúsculas:", textoMinusculas);
console.log("Texto fatiado:", textoFatiado);
console.log("Texto substituído:", textoSubstituido);